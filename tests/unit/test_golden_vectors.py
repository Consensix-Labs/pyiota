# 12. Ed25519 keypair and signature (deterministic seed)
# ============================================================


class TestSignatureVectors:
    """Verify our Ed25519 signing matches the TS SDK for a known keypair.

    Uses the deterministic seed 0x00..0x1f so both SDKs produce the
    same public key, address, and signature.
    """

    def test_public_key(self, vectors):
        from pyiota.crypto.ed25519 import Ed25519Keypair

        kp = Ed25519Keypair.from_secret_key(bytes(range(32)))
        assert kp.public_key.hex() == vectors["ed25519_public_key"]

    def test_address_derivation(self, vectors):
        from pyiota.crypto.ed25519 import Ed25519Keypair

        kp = Ed25519Keypair.from_secret_key(bytes(range(32)))
        assert kp.address == vectors["ed25519_address"]

    def test_intent_hash(self, vectors):
        """Blake2b-256(intent_prefix || tx_data_bcs) must match the TS SDK."""
        from pyiota.crypto.signature import hash_intent_message, make_intent_message

        tx = _build_test_tx_data()
        tx_bcs = tx.serialize()
        # Sanity: our tx_data_bcs matches the TS SDK's
        assert tx_bcs.hex() == vectors["tx_data_bcs"]

        intent_msg = make_intent_message(tx_bcs)
        intent_hash = hash_intent_message(intent_msg)
        assert intent_hash.hex() == vectors["intent_hash"]

    def test_raw_signature(self, vectors):
        """Ed25519 signature over the intent hash must match byte-for-byte."""
        from pyiota.crypto.ed25519 import Ed25519Keypair
        from pyiota.crypto.signature import hash_intent_message, make_intent_message

        kp = Ed25519Keypair.from_secret_key(bytes(range(32)))
        tx = _build_test_tx_data()
        intent_msg = make_intent_message(tx.serialize())
        intent_hash = hash_intent_message(intent_msg)

        sig = kp.sign(intent_hash)
        assert sig.hex() == vectors["ed25519_signature"]

    def test_serialized_signature(self, vectors):
        """Full serialized signature (flag || sig || pubkey, base64) must match."""
        from pyiota.crypto.ed25519 import Ed25519Keypair

        kp = Ed25519Keypair.from_secret_key(bytes(range(32)))
        tx = _build_test_tx_data()
        serialized = kp.sign_transaction(tx.serialize())
        assert serialized == vectors["serialized_signature_base64"]
