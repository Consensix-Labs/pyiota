@pytest.fixture(scope="module")
def vectors() -> dict[str, str]:
    """Load golden vectors generated by the official IOTA TypeScript SDK."""
    path = Path(__file__).parent / "golden_vectors.json"
    with open(path) as f:
        return json.load(f)


# -- Helpers --


def _addr_bytes(hex_pattern: str) -> bytes:
    """Convert a 0x-prefixed hex address to 32 bytes, zero-padded."""
    return bytes.fromhex(hex_pattern.removeprefix("0x").zfill(ADDRESS_LENGTH * 2))


def _digest_bytes() -> bytes:
    """The known test digest: bytes 0x00..0x1f."""
    return bytes(range(32))


def _digest_base58(vectors: dict[str, str]) -> str:
    """The Base58-encoded form of the test digest, from the TS SDK."""
    return vectors["object_digest_base58_input"]


