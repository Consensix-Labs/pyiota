# 10. Full TransactionData
# ============================================================


def _build_test_tx_data(*, expiration_epoch: int | None = None) -> TransactionData:
    """Build the canonical test transaction used across golden vector tests."""
    sender = _addr_bytes("0x" + "cc" * 32)
    digest = _digest_bytes()

    ptb = ProgrammableTransaction(
        inputs=[
            CallArg(kind=CallArgKind.PURE, pure_data=serialize_pure_u64(1_000_000_000)),
            CallArg(kind=CallArgKind.PURE, pure_data=serialize_pure_address("0x" + "dd" * 32)),
        ],
        commands=[
            Command(
                kind=CommandKind.SPLIT_COINS,
                split_coin=Argument(kind=ArgumentKind.GAS_COIN),
                split_amounts=[Argument(kind=ArgumentKind.INPUT, index=0)],
            ),
            Command(
                kind=CommandKind.TRANSFER_OBJECTS,
                transfer_objects=[Argument(kind=ArgumentKind.RESULT, index=0)],
                transfer_recipient=Argument(kind=ArgumentKind.INPUT, index=1),
            ),
        ],
    )

    gas = GasPayment(
        payment=[
            BcsObjectRef(
                object_id=_addr_bytes("0x" + "ee" * 32),
                version=10,
                digest=digest,
            )
        ],
        owner=sender,
        price=1000,
        budget=50_000_000,
    )

    return TransactionData(
        kind=ptb,
        sender=sender,
        gas=gas,
        expiration_epoch=expiration_epoch,
    )


class TestTransactionDataVectors:
    def test_full_transaction_no_expiry(self, vectors):
        """Complete TransactionData V1 with no expiration."""
        tx = _build_test_tx_data()
        assert tx.serialize().hex() == vectors["transaction_data_full"]

    def test_full_transaction_with_expiry(self, vectors):
        """Complete TransactionData V1 with epoch expiration."""
        tx = _build_test_tx_data(expiration_epoch=100)
        assert tx.serialize().hex() == vectors["transaction_data_with_expiry"]


