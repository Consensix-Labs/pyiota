class TestTypeTagVectors:
    """TypeTag variant indices follow the IOTA BCS schema order, which
    differs from numeric order: bool=0, u8=1, u64=2, u128=3, address=4,
    signer=5, vector=6, struct=7, u16=8, u32=9, u256=10."""

    @pytest.mark.parametrize(
        "type_str,key",
        [
            ("bool", "type_tag_bool"),
            ("u8", "type_tag_u8"),
            ("u16", "type_tag_u16"),
            ("u32", "type_tag_u32"),
            ("u64", "type_tag_u64"),
            ("u128", "type_tag_u128"),
            ("u256", "type_tag_u256"),
            ("address", "type_tag_address"),
            ("signer", "type_tag_signer"),
        ],
    )
    def test_primitive_types(self, vectors, type_str, key):
        assert _serialize_type_tag(type_str).hex() == vectors[key]

    def test_vector_u8(self, vectors):
        assert _serialize_type_tag("vector<u8>").hex() == vectors["type_tag_vector_u8"]

    def test_struct_iota(self, vectors):
        """0x2::iota::IOTA -- a simple struct with no generics."""
        assert _serialize_type_tag("0x2::iota::IOTA").hex() == vectors["type_tag_struct_iota"]

    def test_struct_coin_with_generic(self, vectors):
        """0x2::coin::Coin<0x2::iota::IOTA> -- struct with one type parameter."""
        result = _serialize_type_tag("0x2::coin::Coin<0x2::iota::IOTA>")
        assert result.hex() == vectors["type_tag_struct_coin"]

    def test_vector_of_struct(self, vectors):
        """vector<0x2::coin::Coin<0x2::iota::IOTA>> -- nested generics."""
        result = _serialize_type_tag("vector<0x2::coin::Coin<0x2::iota::IOTA>>")
        assert result.hex() == vectors["type_tag_vector_coin"]

    def test_multi_generic(self, vectors):
        """0x1::pair::Pair<u64, address> -- struct with two type params."""
        result = _serialize_type_tag("0x1::pair::Pair<u64, address>")
        assert result.hex() == vectors["type_tag_multi_generic"]


