"""Golden vector tests for BCS serialization.

These tests assert that our Python BCS output matches the official IOTA
TypeScript SDK (@iota/iota-sdk) byte-for-byte. The expected values in
golden_vectors.json were generated by generate.cjs using the TS SDK.

This is the highest-value test file in the project -- if the hand-rolled
BCS serialization diverges from the official SDK, transactions will be
rejected by IOTA nodes.
"""

